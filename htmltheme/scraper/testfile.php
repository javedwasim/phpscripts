         * @return {jQuery.Deferred} promise
         */
        var getFilterOptions = function(filterName) {
            // If we already have the options for this filter type, return them immediately
            if(taffyFilterOptionDbs[filterName] != null) {
                return jQuery.Deferred().resolve(taffyFilterOptionDbs[filterName]).promise();
            }

            // Return promise based on ajax call to retrieve the options
            return jQuery.ajax({
                    'url': '/stats/ajax_filter_options_json/'+filterName,
                    'type': 'get'
                })
                // Pipe deprecated in jQuery 1.8, but for 1.7 we use to so that we can replace the
                // jQuery.ajax promise with a new one that contains a TaffyDB instead of the raw ajax
                // response.
                .pipe(function(results) {
                    // cache options as a TAFFY db
                    taffyFilterOptionDbs[filterName] = TAFFY(results);

                    // Return the cached DB promise
                    return getFilterOptions(filterName);
                });
        };

