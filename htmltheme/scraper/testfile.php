                    var inputVal = inputEl.val();
                    if(inputVal !== '') {
                        // Create hidden input array, one input for each selection, to mimic the
                        // behavior we would see on the server-side for a multi-select input.
                        var selectionValues = inputVal.split(',');
                        jQuery.each(selectionValues, function(vIdx, val) {
                            inputEl.after('<input type="hidden" name="' + inputEl.attr('name') + '" value="' + val + '" />');
                        });
                    }
                });

                // Remove the hidden inputs whose values are strings with comma-separated values now
                // that we've replaced them with an array of hidden inputs.
                filterInputs.remove();
