$(function() {
    var isMenuActive = false;
    var isTouched = false;
    // slice off the first nav element which links to the snapshot page and doesn't have a dropdown
    var navMenu = $('.nav-item').slice(1);

    $.each(navMenu, function() {
    	// touchstart is a mobile/tablet specific event, prevent default click functionality and open the menu
    	$(this).on('touchstart', function(e) {
	    	e.stopPropagation();
	    	e.preventDefault();
	    	// menu already open from selecting another nav
	    	if(isMenuActive) {
            	navMenu.trigger('mouseleave');
        	}
	        isMenuActive = true;
	        newActive($(this),isTouched);
    	});
    });

    // menuActive class will make list visible on mobile/tablet

    function removeActive(item, callback) {
    	if (item != null) {
    		$(item).find('.menu-active').removeClass('menu-active');
    	} else {
    		$('.menu-active').removeClass('menu-active');
    	}

        callback();
    }

    function newActive($this, isTouched) {
        removeActive($this, function() {
            $this.next().addClass('menu-active').queue(function() {
                if(isTouched) {
                    isMenuActive = true;
                    isTouched = false;
                } else {
                    isTouched = true;
                }
            }).dequeue();
        });
    }

    $(navMenu).on({
        mouseenter:function() {
            newActive($(this),true);
        },
        click:function(e) {
            e.preventDefault();
            if(isMenuActive) {
                $(this).trigger('trueClick',e.target);
            }
        },
        trueClick:function(e,$target) {
            $(this).parents('#nav').trigger('mouseleave');
            window.location.href = $target;
        }
    },'li .has-children').on('mouseleave',function() {
        removeActive(null, function() {
            isMenuActive = false;
            isTouched = false;
        });
    });

    $('html').on('touchstart',function(e) {
        if(isMenuActive) {
            navMenu.trigger('mouseleave');
        }
    });
});

