                    filterStatusRestrictions[filterName] = statusVal;
                }
                else {
                    // Clear out any previous status restriction
                    delete filterStatusRestrictions[filterName];
                }
            });

            // If any checkboxes are checked initially (report is run and this is loading the results),
            // trigger the change function for all checked filter boxes so they will load the options
            // and show the selections.
            reportFilterCheckboxContainer.find('input.report-filter-checkbox:checked').change();
