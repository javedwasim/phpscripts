            // Set up all the select2 for all the filters
            var select2Inputs = jQuery('input.input_filter');
            select2Inputs.each(function(idx, hiddenInput) {
                var select2Input = jQuery(hiddenInput);

                // Grab the filter name (Offers, Affiliates, etc.) that this input collects
                var filterName = select2Input.data('filtername');

                // select2 formatter for results and selections.
                // Some filter types such as offers and affiliates have additional information that we would
                // like to show in their selections (id, status).
                var filterOptionsFormatter = function(optionRow) {
                    switch(filterName) {
                        case 'Offers':
                            return _.escape(optionRow.text) + ' (Offer ID: ' + optionRow.id + ')';
                        case 'Affiliates':
                            return _.escape(optionRow.text) + ' (Affiliate ID: ' + optionRow.id + ')';
                        case 'AdCampaigns':
                            return _.escape(optionRow.text) + ' (Campaign ID: ' + optionRow.id + ')';
                        default:
                            // Everything else just return the display/text
                            return _.escape(optionRow.text);
                    }
                };

                // Initialize the select2 multi-select for this input
                select2Input.select2({
                    'multiple': true,
                    'minimumInputLength': 0,
                    'closeOnSelect': false,
                    'width': 'off',
                    // Load more results before we reach the bottom
                    'loadMorePadding': 300,
                    // Custom formatter to enhance display for certain filter types
                    'formatResult': filterOptionsFormatter,
                    'formatSelection': filterOptionsFormatter,
                    // Set any initial values
                    initSelection: function(element, callback) {
                        // Retrieve any initial selections for this input
                        var initialCommaSeparatedIds = select2Input.val();

                        // Register each of the existing values as selected in the input
                        if(initialCommaSeparatedIds !== '') {
                            // Load the options and then set the selections to those having the ID
                            getFilterOptions(filterName).then(function(optionsDb) {
                                var optionIds = initialCommaSeparatedIds.split(',');
                                var selectedOptions = [];

                                // Retrieve all rows from our TaffyDB which are an option having one of
                                // the specified IDs or are an optgroup with a child option having one
                                // of the specified IDs.
                                var matchingRows = optionsDb(
                                    // Search across options and optgroups (filters in array are OR-ed)
                                    [
                                        // top level option.id search (optgroups don't have id
                                        // so none of them will match this)
                                        {'id': {'==': optionIds}},

                                        // options in an optgroup
                                        function() {
                                            return this.children != null &&
                                                _.any(this.children, function(childOpt) {
                                                    return optionIds.indexOf(childOpt.id.toString()) >= 0;
                                                });
                                        }
                                    ]
                                ).get();

                                // Now that we have matching rows, grab the selections out of them; for
                                // top-level options the row is the selection, but for options nested in
                                // optgroups, the selection is one of the children elements in the row.
                                _.each(matchingRows, function(row) {
                                    if(row.id != null) {
                                        // This is an option; add the row directly as it is the option.
                                        selectedOptions.push(row);
                                    }
                                    else {
                                        // This is an optgroup; locate the nested child row(s)
                                        // that were selected (there could be >1 if multiple options
                                        // from the optgorup were selected)
                                        var selectedChildRows = _.filter(row.children, function(childOption) {
                                            return optionIds.indexOf(childOption.id.toString()) >= 0;
                                        });

                                        // Attach the matching children rows.
                                        Array.prototype.push.apply(selectedOptions, selectedChildRows);
                                    }
                                });

                                // Important: clear out the input value so that if any IDs don't correspond
                                // to a valid option, the id won't get submitted with the form.
                                // Select2 will automatically rebuild the value as a comma-separated list
                                // of IDs when we execute the callback below.
                                select2Input.val('');

                                // Have select2 set the options we just loaded based on existing
                                // IDs (will cause the value to be set as a comma-separated list
                                // of IDs and have it display the selections).
                                callback(selectedOptions);
                            });
                        }
                    },
                    // Delay execution of the query function until 400ms of inactivity have
                    // passed
                    'query': _.debounce(
                        function(options) {
                            getFilterOptions(filterName).then(function(optionsDb) {
                                // Specify size of each result page
                                var pageSize = 500;

                                // Determine pagination start location based on page number.
                                // This is 1-indexed instead of 0-indexed
                                var beginPageRowNum = 1 + ((options.page - 1) * pageSize);

                                // Search across options and optgroups (filters in array are OR-ed).
                                // Search for any rows whose id is the same as the query OR whose display
                                // contains the case-insensitive search term.
                                var taffyQuery =
                                    [
                                        // top level option.id search (optgroups don't have id
                                        // so none of them will match this)
                                        {'id': {'==': options.term}},

                                        // optgroup.text or top level option text (both options
                                        // and optgroups have text so this will match either)
                                        {'text': {'likenocase': options.term}},

                                        // options in an optgroup
                                        // TaffyDB cannot run queries against nested complex
                                        // objects/array so we replicate likenocase functionality
                                        // for options nested in the 'children' array of optgroups.
                                        function() {
                                            return this.children != null &&
                                                _.any(this.children, function(childOpt) {
                                                    return childOpt.id.toString() === options.term ||
                                                        childOpt.text.toLowerCase().indexOf(options.term.toLowerCase()) >= 0
                                                });
                                        }
                                    ];

                                // Restrict top-level options to a specific status if one specified for
                                // this filter type
                                var filteredDb;
                                if(filterStatusRestrictions[filterName] != null) {
                                    // A status filter exists, so restrict results to those with its status
                                    filteredDb = optionsDb({'status': filterStatusRestrictions[filterName]}, taffyQuery);
                                }
                                else {
                                    // No runtime status filter exists, so execute the base query
                                    filteredDb = optionsDb(taffyQuery);
                                }

                                // Retrieve the paginated results for the query.
                                var searchResults = filteredDb
                                    .start(beginPageRowNum)
                                    // Retrieve 1 more than the needed result count so we can determine if
                                    // there are any more pages after this one.
                                    .limit(pageSize+1)
                                    .get();

                                options.callback({
                                    // If we found the extra row, we know there are additional results
                                    'more':    searchResults.length > pageSize,
                                    // Ignore the extra row retrieved
                                    'results': searchResults.slice(0, pageSize)
                                });
                            });
                        },
                        400
                    )
                });
            });

            // Enable filter tabs to show/hide whenever checkbox is clicked
            reportFilterCheckboxContainer.on('change', 'input.report-filter-checkbox', function() {
