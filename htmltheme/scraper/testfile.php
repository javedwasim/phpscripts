                    var filterInput = filterPanel.find('input.input_filter');
                    getFilterOptions(filterInput.data('filtername')).then(function() {
                        // Done loading so hide indicator and show the select2 input
                        loadingIndicator.hide();

                        // Show the dropdown
                        filterPanel.find('div.filter-dropdown-container').show();
                    });
                }
                else {
                    // Hide the filter container since the checkbox was unchecked
                    filterPanel.hide();
                }
            });

            // Add listener to any filter status toggles (Offer, Affiliate) so that search results for
            // those dropdowns will be limited to the appropriate status results
            reportFilterSelectionsContainer.on('change', 'input.filter-status-toggle', function() {
